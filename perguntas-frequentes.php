        <div class="page-header">
          <h1>Perguntas Frequentes</h1>
        </div>
        
        <h3>Como faço para me inscrever no programa?</h3>
        <p>O aluno candidato deverá inscrever - se no site do Ciência Sem Fronteiras pelo seguinte link: http://www.cienciasemfronteiras.gov.br/web/csf Deverá ser feita também a inscrição pelo site da PUCPR no link: http://www2.pucpr.br/reol/cienciasemfronteiras/ É necessária a inscrição nesses dois momentos para que toda e qualquer informação sobre a candidatura do aluno seja disponibilizada a Coordenação do Ciência sem Fronteiras da PUCPR  (CSF). Sendo assim, poderemos acompanhar o processo de inscrição e aprovação de cada aluno para assim esclarecer qualquer dúvida que se faça necessária.</p>
        <br />

        <h3>Quem inscreve os candidatos? O coordenador responsável ou o próprio candidato?</h3>
        <p>O próprio candidato é responsável pela sua inscrição tanto no site do governo como no site da PUCPR. O coordenador é responsável por divulgar as chamadas, prazos e procedimentos específicos. Após o término das inscrições a CAPES/CNPq enviam as inscrições realizadas para que o coordenador faça a homologação.</p>
        <br />

        

        <h3>O meu curso de graduação se inclui dentro das áreas prioritárias?</h3>
        <p>A relação das áreas consideradas prioritárias no programa do Ciência sem Fronteiras pode ser consultada no Portal do Programa.</p>
        <br />

        <h3>Há exigência de proficiência em língua estrangeira? Quais exames de proficiência em língua estrangeira serão aceitos?</h3>
        <p>A exigência será feita conforme acordo realizado entre as agências (CNPq e CAPES) e a instituição de ensino estrangeira. Leia com atenção o texto completo da Chamada ao País que você está se candidatando. Cada chamada tem suas regras específicas. </p>
        <br />

        <h3>Quais Universidades no exterior podem participar do programa?</h3>
        <p>As Universidades de excelência no exterior  firmaram acordos com os representantes Educacionais (parceiros) de cada país ou com o CNPq e a CAPES. O parceiro de cada chamada é que disponibilizará a relação das Universidades participantes do Programa.</p>
        <br />

        <h3>Quais são as áreas áreas prioritárias que posso concorrer?</h3>
        <p>Temas e áreas de interesse: Engenharias e demais áreas tecnológicas; Ciências Exatas e da Terra: Física, Química, Biologia e Geociências, Ciências Biomédicas e da Saúde; Computação e tecnologias da informação; Tecnologia Aeroespacial; Fármacos; Produção Agrícola Sustentável; Petróleo, Gás e Carvão Mineral; Energias Renováveis; Tecnologia Mineral; Biotecnologia; Nanotecnologia e Novos materiais; Tecnologias de Prevenção e Mitigação de Desastres Naturais; Biodiversidade e Bioprospecção; Ciências do Mar; Indústria criativa; Novas Tecnologias de Engenharia Construtiva; Formação de Tecnólogos. Veja se há restrição de alguma área prioritária na chamada de seu interesse. </p>
        <br />

        <h3>Quem decide se o curso do candidato está dentro das áreas prioritárias?</h3>
        <p>A CAPES/CNPq analisam se o curso de graduação do candidato está ou não incluído entre as áreas prioritárias.</p>
        <br />

        <h3>Os estudantes e pesquisadores beneficiados pelo programa só poderão atuar dentro das áreas prioritárias do CsF?</h3>
        <p>Sim. Tanto estudantes como pesquisadores deverão seguir as áreas prioritárias, constantes do Programa.</p>
        <br />

        <h3>Ainda não tenho o resultado do Teste de Proficiência, posso enviar depois?</h3>
        <p>Leia com atenção o cronograma da chamada que está se candidatando e siga rigorosamente o prazo estabelecido. Geralmente é possível enviar posteriormente ao término da inscrição o  resultado do teste de proficiência</p>
        <br />


        <h3>Ainda não concluí a graduação, posso participar do programa?</h3>
        <p>Sim, desde que tenha concluído de 20% a no máximo 90% do curso na instituição de ensino brasileira e esteja devidamente matriculado. Neste caso, a aluno pode pleitear a bolsa Graduação Sanduíche no Exterior (SWG) para fazer intercâmbio de 6 a 12 meses no exterior,de acordo com cada Chamada.</p>
        <br />

        <h3>Sou aluno PIBIC, como devo proceder?</h3>
        <p>Você deverá comunicar a Coordenação do PIBIC da PUCPR sobre a sua aprovação  na Universidade Destino, para que a bolsa  PIBIC seja cancelada. Só assim você poderá dar o aceite a sua bolsa do CSF. O CNPq/CAPES não autorizam o acúmulo de bolsas.</p>
        <br />

        <h3>Onde devo anexar os documentos exigidos na inscrição?</h3>
        <p>Os documentos da inscrição exigidos na chamada pública devem ser anexados dentro do formulário de inscrição disponível no "formulário de inscrições". Todos os documentos deverão anexados em PDF.</p>
        <br />

        <h3>A documentação exigida pode ser enviada por e-mail?</h3>
        <p>Não. As documentações exigidas deverão ser anexadas no site na página do programa, respeitando o respectivo calendário. Lembrando que todos os documentos devem estar no formato PDF.</p>
        <br />

        <h3>Tenho Bolsa auxílio da PUCPR, ProUNI ou FIES, como devo proceder?</h3>
        <p>Você deverá comunicar a Coordenação do CsF caso tenha qualquer auxílio para cursar a graduação na Universidade, seja Bolsa PUCPR, FIES ou ProUNI. Esse auxílio será suspenso pela Coordenação e o aluno deverá comparecer ao SIGA para assinar o termo de "suspensão temporária", assim a bolsa não será cancelada tendo o aluno direito de reativá-la no seu retorno à PUCPR.</p>
        <br />

        <h3>Qual é o valor da bolsa para alunos que vão para o Exterior?</h3>
        <p>No site do Ciência Sem Fronteiras na chamada de cada País tem os valores específicos. Os valores variam de um País para o outro.</p>
        <br />

        <h3>Quando ficarei sabendo para qual instituição irei?</h3>
        <p>O estudante será comunicado por e-mail em qual Universidade foi aceito, bem como os procedimentos para os passos posteriores.</p>
        <br />

        <h3>Posso escolher em que Universidade quero estudar no exterior?</h3>
        <p>Os parceiros que intermedeiam as universidades estrangeiras e as agências responsáveis pelo programa Ciência sem Fronteiras serão os responsáveis por aplicar as universidades, utilizando a pertinência quanto às áreas prioritárias do Programa Ciência sem Fronteiras.</p>
